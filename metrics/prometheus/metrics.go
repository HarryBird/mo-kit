package prometheus

import (
	prom "github.com/prometheus/client_golang/prometheus"
)

var (
	MetricDurationPerRequest = prom.NewHistogramVec(prom.HistogramOpts{
		Namespace: "server",
		Subsystem: "requests",
		Name:      "duration_ms",
		Help:      "server requests duration(ms).",
		Buckets:   []float64{5, 10, 25, 50, 100, 250, 500, 1000},
	}, []string{"kind", "operation"})

	MetricRequestsCodeTotal = prom.NewCounterVec(prom.CounterOpts{
		Namespace: "client",
		Subsystem: "requests",
		Name:      "code_total",
		Help:      "The total number of processed requests",
	}, []string{"kind", "operation", "code", "reason"})
)
